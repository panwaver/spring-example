package com.pwh.leetcode.math;

/**
 * Definition for singly-linked list.
 *
 * @author: Waver Pan
 * @date: 2022/1/18 17:08
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
